    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        
        Produto p = new Produto();
        p.setNome(txNome.getText());
        p.setQntEstoque(txqntEstoque.getText());
        p.setValor(Double.parseDouble(txValor.getText()));
        p.setCategoriaproduto((CategoriaProduto)cxcCategoriapro.getSelectedItem());
        try {
            String data = txData_fabricacao.getText();
            p.setDatafabricacao(new SimpleDateFormat("dd/mm/yyyy").parse(data));
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao converter a data");
        }
       
        
        
        
      
        if (!(txCodigo.getText().equals("")) || (txCodigo.getText().equals(null))) {
            p.setCodigo(Integer.parseInt(txCodigo.getText()));
        }
        ProdutoController pc = new ProdutoController();
        SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
        if (p.getCodigo() == 0) {
            int id = pc.salvar(p);
            modelo.addRow(new Object[]{id, p.getNome(), p.getQntEstoque(), p.getValor(),sdf.format(p.getDatafabricacao()), p.getCategoriaproduto()});
            JOptionPane.showMessageDialog(null, " Produto cadastrado com sucesso");
            
        } else {
            int id = pc.salvar(p);
            if (id > 0) {
                 modelo.removeRow(linhaSelecionada);
                modelo.addRow(new Object[]{id, p.getNome(), p.getQntEstoque(), p.getValor(), sdf.format(p.getDatafabricacao()), p.getCategoriaproduto()});
                JOptionPane.showMessageDialog(null, "Produto editado com sucesso");
            }
        }
        dispose();
    }//GEN-LAST:event_btSalvarActionPerformed
    
