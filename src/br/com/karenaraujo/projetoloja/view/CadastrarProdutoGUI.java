    private void carregarCombo() {
        
        DefaultComboBoxModel combomodel = (DefaultComboBoxModel) cxcCategoriapro.getModel();
        //Remover todos os itens do combo
        combomodel.removeAllElements();

        //Buscar na base de dados os cursos cadastrados
        List<CategoriaProduto> produtos = new ArrayList<>();
        CategoriaProdutoController cpc = new CategoriaProdutoController();
        produtos = cpc.listarTodos();

        //Preencher o combo com os cursos que estão na lista
        for (int linha = 0; linha < produtos.size(); linha++) {
            CategoriaProduto cp = produtos.get(linha);
            combomodel.addElement(cp);
            
        }
    }
    
    private void carregarCombo(CategoriaProduto catepro) {
        DefaultComboBoxModel combomodel = (DefaultComboBoxModel) cxcCategoriapro.getModel();
        //Remover todos os itens do combo
        combomodel.removeAllElements();

        //Buscar na base de dados os cursos cadastrados
        List<CategoriaProduto> produtos = new ArrayList<>();
        CategoriaProdutoController cpc = new CategoriaProdutoController();
        produtos = cpc.listarTodos();

        //Preencher o combo com os cursos que estão na lista
        for (int linha = 0; linha < produtos.size(); linha++) {
            CategoriaProduto cp = produtos.get(linha);
            combomodel.addElement(cp);
            if (catepro.getCodigo() == cp.getCodigo()) {
                combomodel.setSelectedItem(cp);
            }
        }
    }
